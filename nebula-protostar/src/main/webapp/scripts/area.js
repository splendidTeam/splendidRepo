function addCity(event,proIndex){
    if(proIndex == undefined){
        proIndex = $('#_province').val();
    }
    $('#_city>option').remove();
    var op = $("<option value=''>请选择市/区-</option>");
    $("#_district").css('visibility', 'hidden');
    op.appendTo($('#_city'));    
    $("#_district").css('visibility', 'visible');
    if(city[proIndex] != undefined){
    	for(var i=0; i< city[proIndex].length; i++){
            var op = $('<option value='+ i + '>'+  city[proIndex][i].city +'</option>');
            op.appendTo($('#_city'));
        }    	
    }    
    $('#_district>option').remove();
    var op = $("<option value=''>请选择地区-</option>");
    op.appendTo($('#_district'));
}

