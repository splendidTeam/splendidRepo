package com.baozun.nebula.command.i18n;
public class SingleLang  extends  LangProperty{
	
	private static final long serialVersionUID = 8663008726653415063L;
	
	private String value;

	private String lang;

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

	public String getLang() {
		return lang;
	}

	public void setLang(String lang) {
		this.lang = lang;
	}
	
	
	
}

