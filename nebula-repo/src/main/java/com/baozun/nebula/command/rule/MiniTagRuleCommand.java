package com.baozun.nebula.command.rule;

import com.baozun.nebula.model.BaseModel;
 
/**
 * 迷你的标签规则类<br>
 * 用于表达式解析
 * 
 * @author 项硕
 */
public class MiniTagRuleCommand extends BaseModel {  
	
	private static final long serialVersionUID = -9200985202663980674L;

	/** 会员、分组或组合的id */
	private Long id;

	/** 会员组合类型：1会员列表，2会员分组，4组合 */
	private Integer type;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Integer getType() {
		return type;
	}

	public void setType(Integer type) {
		this.type = type;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		result = prime * result + ((type == null) ? 0 : type.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		MiniTagRuleCommand other = (MiniTagRuleCommand) obj;
		if (id == null) {
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id))
			return false;
		if (type == null) {
			if (other.type != null)
				return false;
		} else if (!type.equals(other.type))
			return false;
		return true;
	}
	
}
