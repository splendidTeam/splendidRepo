package com.baozun.nebula.utilities.integration.oauth.exception;

public class RequestInfoException extends ThirdPartyException{

	/**
	 * 
	 */
	private static final long serialVersionUID = -816924193265740420L;

	public RequestInfoException() {
		super();
	}

	public RequestInfoException(String arg0, Throwable arg1) {
		super(arg0, arg1);
	}

	public RequestInfoException(String arg0) {
		super(arg0);
	}

	public RequestInfoException(Throwable arg0) {
		super(arg0);
	}

	
}
